from flask import Flask, jsonify

app = Flask(__name__)

@app.route("/")
def home():
    return jsonify({"message": "Welcome to the IELTS Speaking Test Platform!"})
@app.route("/info")
def info():
    return jsonify({
        "platform": "IELTS Speaking Test API",
        "version": "1.0",
        "developer": "Nikitha"
    })
if __name__ == "__main__":
    app.run(host="0.0.0.0", port=5001, debug=True)







 

  
               
                        
                              
                                   





























































































































































































  


   
    
                  
 
































































